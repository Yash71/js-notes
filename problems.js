//Convert HTML Entities
function convertHTML(str) {
    let array = str.split("");
    for (let i = 0; i < array.length; i++) {
        switch (array[i]) {
            case "<":
                array[i] = "&lt;";
                break;
            case ">":
                array[i] = "&gt;";
                break;
            case "'":
                array[i] = "&apos;";
                break;
            case '"':
                array[i] = "&quot;";
                break;
            case "&":
                array[i] = "&amp;";
                break;
        }
    }
    return array.join("");
}
// Sum All Odd Fibonacci Numbers
function sumFibs(num) {
    //method 1
    if (num <= 0) return 0;
    const arrFib = [1, 1];
    let nextFib = 0;
    while ((nextFib = arrFib[0] + arrFib[1]) <= num) {
        arrFib.unshift(nextFib);
    }
    return arrFib.filter(x => x % 2 != 0).reduce((a, b) => a + b);
    //method 2
    let prevNumber = 0;
    let currNumber = 1;
    let result = 0;
    while (currNumber <= num) {
        if (currNumber % 2 !== 0) {
            result += currNumber;
        }
        currNumber += prevNumber;
        prevNumber = currNumber - prevNumber;
    }

    return result;
}